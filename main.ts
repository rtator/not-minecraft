scene.onHitWall(SpriteKind.Player, function (sprite) {
    if (controller.A.isPressed()) {
        if (controller.left.isPressed()) {
            for (let direction of [CollisionDirection.Left]) {
                breakTileInDirection(direction)
            }
        } else if (controller.right.isPressed()) {
            for (let direction of [CollisionDirection.Right]) {
                breakTileInDirection(direction)
            }
        } else if (controller.up.isPressed()) {
            for (let direction of [CollisionDirection.Top]) {
                breakTileInDirection(direction)
            }
        } else if (controller.down.isPressed()) {
            for (let direction of [CollisionDirection.Bottom]) {
                breakTileInDirection(direction)
            }
        }
    }
})
function breakTileInDirection (direction: number) {
    locationOfWall = tilemap.locationInDirection(tilemap.locationOfSprite(mySprite), direction)
    if (tilemap.tileIsWall(locationOfWall) && !(tilemap.tileIs(locationOfWall, myTiles.tile5)) && !(tilemap.tileIs(locationOfWall, myTiles.tile2)) && !(tilemap.tileIs(locationOfWall, myTiles.tile1))) {
        tiles.setWallAt(locationOfWall, false)
        tiles.setTileAt(locationOfWall, myTiles.tile0)
    }
}
